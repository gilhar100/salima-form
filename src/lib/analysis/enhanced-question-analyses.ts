import { generateVariations } from './variation-engine';

export interface EnhancedQuestionAnalysis {
  questionId: number;
  dimension: string;
  text: string;
  analyses: {
    above3?: string[];
    below3?: string[];
    at3AndAbove?: string[];
    at3AndBelow?: string[];
  };
}

// יצירת ניתוחים משופרים עם וריאציות מתקדמות
const createEnhancedAnalysis = (questionId: number, dimension: string, text: string, originalAnalyses: any): EnhancedQuestionAnalysis => {
  const enhanced: EnhancedQuestionAnalysis = {
    questionId,
    dimension,
    text,
    analyses: {}
  };

  // יצירת 5 וריאציות איכותיות לכל סוג ניתוח
  if (originalAnalyses.above3) {
    enhanced.analyses.above3 = generateVariations(originalAnalyses.above3, 5);
  }
  if (originalAnalyses.below3) {
    enhanced.analyses.below3 = generateVariations(originalAnalyses.below3, 5);
  }
  if (originalAnalyses.at3AndAbove) {
    enhanced.analyses.at3AndAbove = generateVariations(originalAnalyses.at3AndAbove, 5);
  }
  if (originalAnalyses.at3AndBelow) {
    enhanced.analyses.at3AndBelow = generateVariations(originalAnalyses.at3AndBelow, 5);
  }

  return enhanced;
};

